#include <stdint.h>
#include <unistd.h>

void	mt_putbyte(uint8_t byte)
{
	int	i;

	i = 0;
	while (++i <= 8)
	{
		if (byte & (1 << (8 - i)))
			write(1, "1", 1);
		else
			write(1, "0", 1);
	}
	write(1, "\n", 1);
}
